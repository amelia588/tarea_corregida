# pedir al usuario que ingrese los dos numeros a sumar
num1 = float(input("ingrese el primer numero: "))
num2 = float(input("ingrese el segundo numero: "))

# sumar los dos numeros
suma =num1 + num2

# mostrar el resultado de la suma
print("la suma es", num1, "y", num2, "es igual a:", suma)

# multiplicar los dos numeros
producto =num1 * num2

# mostrar el resultado de la multiplicacion
print("el producto es", num1, "y", num2, "es igual a:", producto)

# dividir los dos numeros
cociente = num1 /num2
# mostrar el resultado de la division
print("el resultado de dividir",num1, "y",num2, "es igual a:", cociente)